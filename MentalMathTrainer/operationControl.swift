import Foundation

class operationControl {
    
    func addition(a: Int!, b: Int!) -> Int! {
        return a+b
    }
    
    func subtraction(a: Int!, b: Int!) -> Int! {
        return a-b
    }
    
    func multiplication(a: Int!, b: Int!) -> Int! {
        return a*b
    }
    
    func division(a: Int!, b: Int!) -> Int! {
        return a/b
    }
    
    
    
    
    
}
